<h1>Wer sind wir?</h1>
<div class="center">
    <p>
        Hey, was geht? Willkommen bei SunLightScorpion, vorher PlayScorpion genannt. Die Gründung unseres
        Servers begann ursprünglich nur als Mittel mit
        Freunden zu spielen, wobei wir irgendwann dazu übergegangen sind es mehr und mehr als offenen Server
        einzurichten
        und schließlich auch zu veröffentlichen mit dem Namen PlayScorpion. Seit dem Mai 2020 sind
        wir nun als Server aktiv und bieten verschiedene Modis an. Nach ungefähr zwei Jahren sind wir nun als
        SunLightScorpion bekannt. Auch streamen wir ab und zu auf Twitch
        und haben einen TikTok Profil, wo wir den Server und seine Modis weiter vorstellen. Erreichen könnt ihr
        uns unter der IP-Adresse PlayScorpion.de.
