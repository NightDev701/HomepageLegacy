<h1>Über PlayScorpion.de</h1>
<div class="center">
    <p>
        Unser Minecraft Server bietet aktuell SMP (Survival Multiplayer) an.
        Hol dir deine Tägliche Belohnung und löse es in der Crate ein!
        Du kannst entscheiden ob Du alle oder nur einen Teil deiner Level Speichern oder Abrufen willst.
        Deine Meinung ist uns wichtig. Es werden regelmäßig Umfragen gemacht.
        Durch das Ticketsystem kannst Du Ideen oder auch Kritik äußern!
        Größtenteils entwickeln wir unseren Server selbst. Einige Features/Plugins
        haben andere Programmiert. Wir bauen darauf auf was wir haben!
        Noch dieses Jahr wird ein Major Update erscheinen, bei diesem Update handelt es sich um das, "CityBuild" Update!
    </p>
